public class L_Stringkonvensi {
    //mengkonversi tipe data, apapun menjadi sebuah String
    public static void main(String[] args) {
        //konversi tipe data apapun ke String
        double nilai = 75;
        nilai = nilai / 2;
        System.out.println("75 / 2 = " + nilai);
        boolean isOK = true;
        System.out.println("" + isOK);
        System.out.println("" + 25 / 6.0);

    }
}
